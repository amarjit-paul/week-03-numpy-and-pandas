"""
File: array_operations.py
Description: Performing basic operations on NumPy arrays.
"""

import numpy as np

array1 = np.array([1, 2, 3, 4, 5])
array2 = np.array([10, 20, 30, 40, 50])

print("Addition:")
print(array1 + array2)

print("\nSubtraction:")
print(array2 - array1)

print("\nMultiplication:")
print(array1 * array2)

print("\nDivision:")
print(array2 / array1)